@Config
@Autonomous(name = "BLUE_TEST_AUTO_PIXEL", group = "Autonomous")
public class AutoDriveTest extends LinearOpMode {

    @Override
    public void runOpMode() {
        Pose2d initialPose = new Pose2d(-38, -61, Math.toRadians(90));
        MecanumDrive drive = new MecanumDrive(hardwareMap, initialPose);

        // vision here that outputs position
        int visionOutputPosition = 1;

        TrajectoryActionBuilder firstAlign = drive.actionBuilder(initialPose)
                .strafeTo(new Vector2d(-40, -52+14))	//7.5 is half the robot
                .turn(Math.toRadians(-45))
                .lineToY(-56);

        if (isStopRequested()) return;

        Action trajectoryActionChosen;
        trajectoryActionChosen = firstAlign.build();
        Actions.runBlocking(
                new SequentialAction(
                        trajectoryActionChosen
                )
        );

    }
}